void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	t;
	int	j;
	int	s;

	j = size;
	s = 1;
	while (s)
	{
		s = 0;
		i = 1;
		while (i < j)
		{
			if (tab[i] < tab[i - 1])
			{
				t = tab[i];
				tab[i] = tab[i - 1];
				tab[i - 1] = t;
				s = 1;
			}
			i++;
		}
		j--;
	}
}
