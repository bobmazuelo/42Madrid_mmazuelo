int	ft_sqrt(int nb)
{
	int	i;

	if (nb == 1)
		return (1);
	else if (nb <= 0)
		return (0);
	i = 2;
	while (i <= nb / 2)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
