#include "libft.h"
#include <limits.h>

int	ft_atoi(const char *str)
{
	size_t	i;
	int		sign;
	size_t	num;

	i = 0;
	sign = 1;
	num = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	while (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			sign *= -1;
	while (str[i] >= '0' && str[i] <= '9')
		num = (str[i++] & 0xF) + (num * 10);
	if (num > LONG_MAX && sign < 0)
		return (0);
	else if (num > LONG_MAX && sign > 0)
		return (-1);
	return ((int)(num * sign));
}
